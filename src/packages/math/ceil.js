/**
 * @description A delegate to `Math.ceil`.
 * @memberof module:Zoom.Math
 * @function ceil
 * @since v2.1.0
 * @example
 * import { ceil } from '@dustinws/zoom/math';
 *
 * ceil(4.000213); // 5
 *
 * @param  {Number} num The number to round up.
 * @return {Number}
 */
const ceil = num => Math.ceil(num);

export default ceil;
